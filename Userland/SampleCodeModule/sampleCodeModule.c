/* sampleCodeModule.c */
#include <stdint.h>
#include <stdbool.h>
#include <stdio.h>
#include <time.h>
#include <print.h>




int main() {

	uint32_t x = 0;
	puts("HOLA DESDE EL PROGRAMA!\n");

	while (true) {
		KeyStroke c = readKeyStroke();
		if (c.isPrintable) {
			putchar(c.data);
		} else {
			if (c.arrow == ARROW_LEFT) {
				moveCursorLeft();
				x--;
			} else if (c.arrow == ARROW_RIGHT) {
				moveCursorRight();
				x++;
			}
		}
	}

	return 0xDEADBEEF;
}